namespace ControlPanels
{

    /// <summary>
    /// Enumeration for representing state of an animation.
    /// </summary>
    public enum AnimationState
    {
        /// <summary>
        /// The animation is playing.
        /// </summary>
        Playing,

        /// <summary>
        /// The animation is paused.
        /// </summary>
        Paused,

        /// <summary>
        /// The animation is stopped.
        /// </summary>
        Stopped
    }
}
